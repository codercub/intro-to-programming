import turtle as t

# window title
t.title("Stickman")

# head
t.circle(50)

# arms
t.right(30)
t.forward(100)
t.backward(100)
t.right(120)
t.forward(100)
t.backward(100)

# body
t.left(60)
t.forward(150)

# legs
t.right(30)
t.forward(100)
t.backward(100)
t.left(60)
t.forward(100)
t.backward(100)